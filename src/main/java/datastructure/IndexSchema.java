package datastructure;

/**
 * @title: IndexSchema
 * @description: 索引
 */
public class IndexSchema {
	/**
	 * 索引
	 * 线性索引、树形索引、多级索引
	 * 线性索引：稠密索引、分块索引、倒排索引
	 *
	 * 稠密索引：将数据集的每个记录对应一个索引项。
	 * 分块索引：将数据集中的记录分成若干块，块间有序。
	 * 倒排索引：内容中的关键字做索引，并记录这些关键字出现的位置，如：
	 *     关键字    文章编号
	 *     book     1，2
	 *     friend   2，3
	 *     good     1
	 */
}
