package encryption;

/**
 * SHA(Secure Hash Algotithm)
 * SHA与MD5类似，也是一种生成信息摘要的算法。
 */
public class SHA {
	/**
	 * SHA与MD5原理类似。
	 *
	 * SHA算法分类：
	 * 最大的版本分类为SHA-1、SHA-2、SHA-3。
	 * SHA-2包含很多子版本：SHA-224、SHA-256、SHA-384、SHA-512
	 *
	 * SHA-1：可以从明文生成160bit的信息摘要，160/4 = 40(十六进制）
	 *
	 * SHA-256：可以生成长度256bit的信息摘要。
	 * SHA-224：SHA-256的“阉割版”，可以生成长度224bit的信息摘要。
	 * SHA-512：可以生成长度512bit的信息摘要。
	 * SHA-384：SHA-512的“阉割版”，可以生成长度384bit的信息摘要。
	 */
}
